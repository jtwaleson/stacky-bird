    "FINI": {
        symbol: "‚óç",
        description: "Finishes this round",
        execute(board) {
            if (board.validation) {
                let expected = toRaw(board.validation[0].finalStack || []);
                let stack = toRaw(board.stack);
                if (isEqual(expected, stack)) {
                    return board.finish();
                } else {
                    return board.dieBird();
                }
            } else {
                board.finish();
            }
        },
    },
